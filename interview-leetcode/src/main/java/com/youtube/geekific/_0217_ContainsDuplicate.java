package com.youtube.geekific;

import java.util.Arrays;
import java.util.HashSet;
import java.util.Set;
import java.util.stream.Collectors;

/*
 * Video Reference: https://youtu.be/o7WGhB4YCjs
 * LeetCode Reference: https://leetcode.com/problems/contains-duplicate/
 */
public class _0217_ContainsDuplicate {

    public boolean containsDuplicate_bruteForce(int[] nums) {
        for (int i = 0; i < nums.length; i++) {
            for (int j = i + 1; j < nums.length; j++) {
                if (nums[i] == nums[j]) return true;
            }
        }
        return false;
    }

    public boolean containsDuplicate_linear(int[] nums) {
        return nums.length != Arrays.stream(nums).boxed().collect(Collectors.toSet()).size();
    }

    public boolean containsDuplicate_optimal(int[] nums) {
        Set<Integer> set = new HashSet<>();
        for (int num : nums) {
            if (!set.add(num)) return true;
        }
        return false;
    }

}
