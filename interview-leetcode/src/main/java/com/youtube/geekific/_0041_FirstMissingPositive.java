package com.youtube.geekific;

import java.util.Arrays;
import java.util.HashSet;
import java.util.Set;

/*
 * Video Reference: https://youtu.be/-gEAxipisTE
 * LeetCode Reference: https://leetcode.com/problems/first-missing-positive/
 */
public class _0041_FirstMissingPositive {

    public int firstMissingPositive_set(int[] nums) {
        Set<Integer> set = new HashSet<>(Arrays.stream(nums).boxed().toList());
        int i = 1;
        while (true) {
            if (!set.contains(i)) return i;
            i++;
        }
    }

    public int firstMissingPositive_negativeMarking(int[] nums) {
        int n = nums.length;
        for (int i = 0; i < n; i++) {
            if (nums[i] <= 0) nums[i] = n + 1;
        }
        for (int i = 0; i < n; i++) {
            int num = Math.abs(nums[i]);
            if (num > 0 && num <= n) {
                nums[num - 1] = -Math.abs(nums[num - 1]);
            }
        }
        for (int i = 0; i < n; i++) {
            if (nums[i] > 0) return i + 1;
        }
        return n + 1;
    }

}
