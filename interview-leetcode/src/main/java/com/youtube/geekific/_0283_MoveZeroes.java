/*
 * Video Reference: https://youtu.be/cKZIA0IrFc0
 * LeetCode Reference: https://leetcode.com/problems/move-zeroes/
 */
public class _0283_MoveZeroes {

    public void moveZeroes_bruteForce(int[] nums) {
        for (int i = 0; i < nums.length; i++) {
            for (int j = 0; j < nums.length - 1; j++) {
                if (nums[j] == 0) {
                    int temp = nums[j];
                    nums[j] = nums[j + 1];
                    nums[j + 1] = temp;
                }
            }
        }
    }

    public void moveZeroes_linear(int[] nums) {
        int[] arr = new int[nums.length];
        int insertPos = 0;
        for (int num : nums) {
            if (num != 0) {
                arr[insertPos++] = num;
            }
        }
        System.arraycopy(arr, 0, nums, 0, nums.length);
    }

    public void moveZeroes_constantSpace(int[] nums) {
        int insertPos = 0;
        for (int num : nums) {
            if (num != 0) {
                nums[insertPos++] = num;
            }
        }
        while (insertPos < nums.length) {
            nums[insertPos++] = 0;
        }
    }

}
